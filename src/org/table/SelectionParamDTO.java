package org.table;

public class SelectionParamDTO {
	
	private String agentId;
	private String workOrder;
	private String countryPreference;
	private String gender;
	private String languages;
	private String workPrefererence;
	private String workExperience;
	private int yearOfExperience;
	
	public String getAgentId() {
		return agentId;
	}
	public void setAgentId(String agentId) {
		this.agentId = agentId;
	}
	public String getWorkOrder() {
		return workOrder;
	}
	public void setWorkOrder(String workOrder) {
		this.workOrder = workOrder;
	}
	public String getCountryPreference() {
		return countryPreference;
	}
	public void setCountryPreference(String countryPreference) {
		this.countryPreference = countryPreference;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getLanguages() {
		return languages;
	}
	public void setLanguages(String languages) {
		this.languages = languages;
	}
	public String getWorkPrefererence() {
		return workPrefererence;
	}
	public void setWorkPrefererence(String workPrefererence) {
		this.workPrefererence = workPrefererence;
	}
	public String getWorkExperience() {
		return workExperience;
	}
	public void setWorkExperience(String workExperience) {
		this.workExperience = workExperience;
	}
	public int getYearOfExperience() {
		return yearOfExperience;
	}
	public void setYearOfExperience(int yearOfExperience) {
		this.yearOfExperience = yearOfExperience;
	}
	
	

}
